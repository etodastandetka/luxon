class BotConfiguration(models.Model):
    """–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–∫ –±–æ—Ç–∞"""
    key = models.CharField(max_length=100, unique=True, verbose_name="–ö–ª—é—á")
    value = models.TextField(verbose_name="–ó–Ω–∞—á–µ–Ω–∏–µ")
    description = models.CharField(max_length=200, blank=True, verbose_name="–û–ø–∏—Å–∞–Ω–∏–µ")
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        verbose_name = "–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –±–æ—Ç–∞"
        verbose_name_plural = "–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ –±–æ—Ç–∞"
        ordering = ['key']

    def get_json_value(self):
        """–ü–æ–ª—É—á–∏—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ –∫–∞–∫ JSON"""
        try:
            return json.loads(self.value)
        except:
            return self.value

    def set_json_value(self, data):
        """–£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ –∫–∞–∫ JSON"""
        self.value = json.dumps(data, ensure_ascii=False)

    @classmethod
    def get_setting(cls, key, default=None):
        """–ü–æ–ª—É—á–∏—Ç—å –Ω–∞—Å—Ç—Ä–æ–π–∫—É"""
        try:
            setting = cls.objects.get(key=key)
            return setting.get_json_value()
        except cls.DoesNotExist:
            return default

    @classmethod
    def set_setting(cls, key, value, description=""):
        """–£—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –Ω–∞—Å—Ç—Ä–æ–π–∫—É"""
        setting, created = cls.objects.get_or_create(
            key=key,
            defaults={'description': description}
        )
        setting.set_json_value(value)
        setting.save()
        return setting

    def __str__(self):
        return f"{self.key}: {self.value[:50]}..."

