'use client'

import { useEffect, useState } from 'react'
import Link from 'next/link'

interface Request {
  id: number
  userId: string
  username: string | null
  firstName: string | null
  lastName: string | null
  bookmaker: string | null
  accountId: string | null
  bank: string | null
  amount: string | null
  requestType: string
  status: string
  status_detail: string | null
  createdAt: string
}

export default function DashboardPage() {
  const [requests, setRequests] = useState<Request[]>([])
  const [loading, setLoading] = useState(true)
  const [activeTab, setActiveTab] = useState<'pending' | 'deferred'>('pending')

  useEffect(() => {
    fetchRequests()
    
    // –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–∞–∂–¥—ã–µ 3 —Å–µ–∫—É–Ω–¥—ã
    const interval = setInterval(() => {
      fetchRequests(false) // –ù–µ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º loading –ø—Ä–∏ –∞–≤—Ç–æ–æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏
    }, 3000)
    
    // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø—Ä–∏ —Ñ–æ–∫—É—Å–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    const handleVisibilityChange = () => {
      if (!document.hidden) {
        fetchRequests(false)
      }
    }
    
    // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø—Ä–∏ –≤–æ–∑–≤—Ä–∞—Ç–µ —Ñ–æ–∫—É—Å–∞
    const handleFocus = () => {
      fetchRequests(false)
    }
    
    // –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏—è –º–µ–∂–¥—É –≤–∫–ª–∞–¥–∫–∞–º–∏ —á–µ—Ä–µ–∑ storage event
    const handleStorageChange = (e: StorageEvent) => {
      if (e.key === 'request_updated' && e.newValue) {
        const updatedRequestId = parseInt(e.newValue)
        console.log('üîÑ Request updated in another tab:', updatedRequestId)
        fetchRequests(false)
      }
    }
    
    document.addEventListener('visibilitychange', handleVisibilityChange)
    window.addEventListener('focus', handleFocus)
    window.addEventListener('storage', handleStorageChange)
    
    return () => {
      clearInterval(interval)
      document.removeEventListener('visibilitychange', handleVisibilityChange)
      window.removeEventListener('focus', handleFocus)
      window.removeEventListener('storage', handleStorageChange)
    }
  }, [activeTab])

  const fetchRequests = async (showLoading = true) => {
    if (showLoading) {
      setLoading(true)
    }
    try {
      const params = new URLSearchParams()
      if (activeTab === 'pending') {
        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ –æ–∂–∏–¥–∞—é—â–∏–µ –∑–∞—è–≤–∫–∏
        params.append('status', 'pending')
      } else if (activeTab === 'deferred') {
        // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ –æ—Ç–ª–æ–∂–µ–Ω–Ω—ã–µ –∑–∞—è–≤–∫–∏
        params.append('status', 'deferred')
      }

      const response = await fetch(`/api/requests?${params.toString()}`)
      const data = await response.json()

      console.log('üìã Fetched requests data:', data)

      if (data.success) {
        const requestsList = data.data.requests || []
        
        console.log(`‚úÖ Loaded ${requestsList.length} requests for tab: ${activeTab}`)
        setRequests(requestsList)
      } else {
        console.error('‚ùå Failed to fetch requests:', data.error || data)
        setRequests([])
      }
    } catch (error) {
      console.error('‚ùå Failed to fetch requests:', error)
      setRequests([])
    } finally {
      if (showLoading) {
        setLoading(false)
      }
    }
  }

  const getTypeLabel = (type: string) => {
    return type === 'deposit' ? '–ü–æ–ø–æ–ª–Ω–µ–Ω–∏–µ' : '–í—ã–≤–æ–¥'
  }

  const getStatusLabel = (status: string) => {
    switch (status) {
      case 'pending':
        return '–û–∂–∏–¥–∞–µ—Ç'
      case 'completed':
      case 'approved':
      case 'auto_completed':
      case 'autodeposit_success':
        return '–£—Å–ø–µ—à–Ω–æ'
      case 'rejected':
      case 'declined':
        return '–û—Ç–∫–ª–æ–Ω–µ–Ω–æ'
      case 'deferred':
        return '–û—Ç–ª–æ–∂–µ–Ω–æ'
      case 'manual':
      case 'awaiting_manual':
        return '–†—É—á–Ω–∞—è'
      default:
        return status
    }
  }

  const getStatusColor = (status: string) => {
    switch (status) {
      case 'completed':
      case 'approved':
      case 'auto_completed':
      case 'autodeposit_success':
        return 'bg-green-500 text-black border border-green-400'
      case 'pending':
        return 'bg-yellow-500 text-black border border-yellow-400'
      case 'rejected':
      case 'declined':
        return 'bg-red-500 text-white border border-red-400'
      case 'deferred':
        return 'bg-orange-500 text-white border border-orange-400'
      case 'manual':
      case 'awaiting_manual':
        return 'bg-red-500 text-white border border-red-400'
      default:
        return 'bg-gray-700 text-gray-300 border border-gray-600'
    }
  }

  const formatDate = (dateString: string) => {
    const date = new Date(dateString)
    const day = String(date.getDate()).padStart(2, '0')
    const month = String(date.getMonth() + 1).padStart(2, '0')
    const year = date.getFullYear()
    const hours = String(date.getHours()).padStart(2, '0')
    const minutes = String(date.getMinutes()).padStart(2, '0')
    return `${day}.${month}.${year} ‚Ä¢ ${hours}:${minutes}`
  }

  const getBankImage = (bank: string | null) => {
    if (!bank) return null
    const normalized = bank.toLowerCase()
    
    // –ú–∞–ø–ø–∏–Ω–≥ –±–∞–Ω–∫–æ–≤ –Ω–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
    if (normalized.includes('demirbank') || normalized.includes('demir')) {
      return '/images/demirbank.jpg'
    }
    if (normalized.includes('omoney') || normalized.includes('o!money')) {
      return '/images/omoney.jpg'
    }
    if (normalized.includes('balance')) {
      return '/images/balance.jpg'
    }
    if (normalized.includes('bakai')) {
      return '/images/bakai.jpg'
    }
    if (normalized.includes('megapay')) {
      return '/images/megapay.jpg'
    }
    if (normalized.includes('mbank')) {
      return '/images/mbank.png'
    }
    if (normalized.includes('optima')) {
      return '/images/optima.jpg'
    }
    if (normalized.includes('companion')) {
      return '/images/companion.png'
    }
    
    return null
  }

    const getTransactionType = (request: Request) => {
      // –ï—Å–ª–∏ —Å—Ç–∞—Ç—É—Å "–û–∂–∏–¥–∞–µ—Ç", –ø–æ–∫–∞–∑—ã–≤–∞–µ–º "-"
      if (request.status === 'pending' || request.status === 'processing') {
        return '-'
      }
      
      // –ï—Å–ª–∏ –∑–∞—è–≤–∫–∞ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∞ –∏–ª–∏ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∞ –≤—Ä—É—á–Ω—É—é, –Ω–µ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º "–ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ"
      if (request.status === 'rejected' || request.status === 'declined') {
        return request.requestType === 'deposit' ? '–ü–æ–ø–æ–ª–Ω–µ–Ω–∏–µ' : '–í—ã–≤–æ–¥'
      }
      
      // –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–∏–ø —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
      // –ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ - —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ —Å—Ç–∞—Ç—É—Å —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ –∞–≤—Ç–æ–ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ
      if (request.status_detail?.includes('autodeposit') || request.status === 'autodeposit_success' || request.status === 'auto_completed') {
        return '–ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ'
      }
      
      // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ profile-* –≤ status_detail –∏–ª–∏ –¥—Ä—É–≥–∏—Ö –ø–æ–ª—è—Ö
      if (request.status_detail?.match(/profile-\d+/)) {
        return request.status_detail.match(/profile-(\d+)/)?.[0] || 'profile-1'
      }
      
      // –ï—Å–ª–∏ –∑–∞—è–≤–∫–∞ –≤—Ä—É—á–Ω—É—é –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∞ (completed/approved, –Ω–æ –Ω–µ –∞–≤—Ç–æ–ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ) - –ø–æ–∫–∞–∑—ã–≤–∞–µ–º profile-1
      if ((request.status === 'completed' || request.status === 'approved') && request.requestType === 'deposit') {
        return 'profile-1'
      }
      
      // –ï—Å–ª–∏ –µ—Å—Ç—å bookmaker –∏ —ç—Ç–æ –¥–µ–ø–æ–∑–∏—Ç, –Ω–æ —Å—Ç–∞—Ç—É—Å –Ω–µ —É—Å–ø–µ—à–Ω—ã–π –∏–ª–∏ –Ω–µ –∞–≤—Ç–æ–ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ - –º–æ–∂–µ—Ç –±—ã—Ç—å –∞–≤—Ç–æ
      if (request.requestType === 'deposit' && request.bookmaker && 
          (request.status === 'completed' || request.status === 'approved' || request.status === 'autodeposit_success' || request.status === 'auto_completed')) {
        // –ï—Å–ª–∏ —Å—Ç–∞—Ç—É—Å autodeposit - —ç—Ç–æ –∞–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ
        if (request.status === 'autodeposit_success' || request.status === 'auto_completed' || request.status_detail?.includes('autodeposit')) {
          return '–ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ'
        }
        // –ò–Ω–∞—á–µ —ç—Ç–æ –≤—Ä—É—á–Ω—É—é –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–Ω–æ–µ - profile-1
        return 'profile-1'
      }
      
      // –î–ª—è –≤—ã–≤–æ–¥–æ–≤ –º–æ–∂–µ—Ç –±—ã—Ç—å profile-*
      if (request.requestType === 'withdraw') {
        return request.status_detail?.match(/profile-\d+/)?.[0] || 'profile-1'
      }
      
      return request.requestType === 'deposit' ? '–ü–æ–ø–æ–ª–Ω–µ–Ω–∏–µ' : '–í—ã–≤–æ–¥'
    }

  return (
    <div className="py-4">
      {/* –•–µ–¥–µ—Ä —Å –∑–∞–≥–æ–ª–æ–≤–∫–æ–º */}
      <div className="flex items-center justify-between mb-4">
        <div className="w-10"></div>
        <div className="flex-1 text-center">
          <h1 className="text-xl font-bold text-white">–ó–∞—è–≤–∫–∏</h1>
          <p className="text-xs text-gray-300 mt-1">–ê–∫—Ç—É–∞–ª—å–Ω—ã–µ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏</p>
        </div>
        <button 
          onClick={() => fetchRequests()}
          className="p-2 bg-gray-800 rounded-lg"
        >
          <svg className="w-5 h-5 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
          </svg>
        </button>
      </div>

      {/* –¢–∞–±—ã */}
      <div className="flex space-x-2 mb-6">
        <button
          onClick={() => setActiveTab('pending')}
          className={`flex-1 px-4 py-3 rounded-xl font-medium text-sm transition-all ${
            activeTab === 'pending'
              ? 'bg-green-500 text-black shadow-lg'
              : 'bg-gray-800 text-gray-300'
          }`}
        >
          –û–∂–∏–¥–∞—é—â–∏–µ
        </button>
        <button
          onClick={() => setActiveTab('deferred')}
          className={`flex-1 px-4 py-3 rounded-xl font-medium text-sm transition-all ${
            activeTab === 'deferred'
              ? 'bg-green-500 text-black shadow-lg'
              : 'bg-gray-800 text-gray-300'
          }`}
        >
          –û—Ç–ª–æ–∂–µ–Ω–Ω—ã–µ
        </button>
      </div>

      {/* –ö–æ–Ω—Ç–µ–Ω—Ç –∑–∞—è–≤–æ–∫ */}
      {loading ? (
        <div className="flex items-center justify-center py-20">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-green-500"></div>
        </div>
      ) : requests.length === 0 ? (
        <div className="flex flex-col items-center justify-center py-20">
          <div className="w-24 h-24 bg-green-500 rounded-2xl flex items-center justify-center mb-4 shadow-lg">
            <svg className="w-16 h-16 text-black" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
            </svg>
          </div>
          <p className="text-white text-lg font-medium">–ù–µ—Ç –∑–∞—è–≤–æ–∫</p>
        </div>
      ) : (
        <div className="space-y-3">
          {requests.map((request) => {
            const isDeposit = request.requestType === 'deposit'
            const userName = request.username 
              ? `@${request.username}` 
              : request.firstName 
                ? `${request.firstName}${request.lastName ? ' ' + request.lastName : ''}` 
                : `ID: ${request.userId}`
            const transactionType = getTransactionType(request)
            const isDeferred = request.status === 'deferred'
            // –ï—Å–ª–∏ –æ—Ç–ª–æ–∂–µ–Ω–æ –∏ "–ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ", –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –º–∏–Ω—É—Å
            const showMinus = isDeferred && transactionType === '–ê–≤—Ç–æ –ø–æ–ø–æ–ª–Ω–µ–Ω–∏–µ'

            const isPending = request.status === 'pending'
            
            return (
              <Link
                key={request.id}
                href={`/dashboard/requests/${request.id}`}
                className="block bg-gray-800 bg-opacity-50 rounded-xl p-4 border border-gray-700 hover:border-green-500 transition-colors backdrop-blur-sm"
              >
                  <div className="flex items-start justify-between">
                    {/* –õ–µ–≤–∞—è —á–∞—Å—Ç—å: –ò–∫–æ–Ω–∫–∞ –±–∞–Ω–∫–∞ –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ */}
                    <div className="flex items-start space-x-3 flex-1">
                      {/* –ò–∫–æ–Ω–∫–∞ –±–∞–Ω–∫–∞ */}
                      {getBankImage(request.bank) ? (
                        <div className="w-12 h-12 rounded-lg overflow-hidden flex-shrink-0 border border-gray-600 bg-gray-900">
                          <img
                            src={getBankImage(request.bank) || ''}
                            alt={request.bank || 'Bank'}
                            className="w-full h-full object-cover"
                          />
                        </div>
                      ) : (
                        <div className="w-12 h-12 bg-purple-500 rounded-lg flex items-center justify-center flex-shrink-0">
                          <div className="w-10 h-10 bg-green-500 rounded-full flex items-center justify-center">
                            <svg className="w-6 h-6 text-black" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                            </svg>
                          </div>
                        </div>
                      )}

                      {/* –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ –∏ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏ */}
                      <div className="flex-1 min-w-0">
                        <p className="text-base font-bold text-white mb-0.5">
                          {userName}
                        </p>
                        <p className="text-xs text-gray-400 mb-2">
                          {request.accountId ? `ID: ${request.accountId}` : request.bookmaker || '-'}
                        </p>
                        
                        {/* –¢–∏–ø —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏ */}
                        <span className="inline-block px-2 py-0.5 text-xs font-medium bg-blue-500 bg-opacity-20 text-blue-300 rounded-md mb-1 border border-blue-500 border-opacity-30">
                          {transactionType}
                        </span>
                      </div>
                    </div>

                    {/* –ü—Ä–∞–≤–∞—è —á–∞—Å—Ç—å: –î–∞—Ç–∞, —Å—É–º–º–∞ –∏ —Å—Ç–∞—Ç—É—Å */}
                    <div className="flex flex-col items-end space-y-2 ml-4">
                      {/* –î–∞—Ç–∞ –∏ –≤—Ä–µ–º—è */}
                      <p className="text-xs text-gray-400 whitespace-nowrap">
                        {formatDate(request.createdAt)}
                      </p>
                      
                      {/* –°—É–º–º–∞ */}
                      <p
                        className={`text-base font-bold ${
                          showMinus ? 'text-red-500' : (isDeposit ? 'text-green-500' : 'text-red-500')
                        }`}
                      >
                        {showMinus ? '-' : (isDeposit ? '+' : '-')}
                        {request.amount ? parseFloat(request.amount).toLocaleString('ru-RU', {
                          minimumFractionDigits: 2,
                          maximumFractionDigits: 2,
                        }).replace('.', ',') : '0,00'}
                      </p>
                      
                      {/* –°—Ç–∞—Ç—É—Å */}
                      <span
                        className={`inline-flex items-center gap-1 px-2 py-1 rounded-md text-xs font-medium whitespace-nowrap ${getStatusColor(request.status)}`}
                      >
                        <div className={`w-1.5 h-1.5 rounded-full ${
                          getStatusLabel(request.status) === '–£—Å–ø–µ—à–Ω–æ' ? 'bg-green-600' :
                          getStatusLabel(request.status) === '–û—Ç–∫–ª–æ–Ω–µ–Ω–æ' ? 'bg-red-600' :
                          getStatusLabel(request.status) === '–û—Ç–ª–æ–∂–µ–Ω–æ' ? 'bg-orange-600' :
                          'bg-yellow-600'
                        }`}></div>
                        {getStatusLabel(request.status)}
                      </span>
                    </div>
                  </div>
                </Link>
            )
          })}
        </div>
      )}
    </div>
  )
}
