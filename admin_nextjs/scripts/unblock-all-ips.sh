#!/bin/bash
# Скрипт для разблокировки всех IP (очистка блокировок)

echo "🔓 Разблокировка всех IP адресов"
echo ""

cd /var/www/luxon/admin_nextjs || exit 1

echo "⚠️  ВНИМАНИЕ: Это очистит ВСЕ блокировки IP!"
echo "   Это может быть небезопасно, если у вас были реальные атаки."
echo ""
read -p "Продолжить? (yes/no): " confirm

if [ "$confirm" != "yes" ]; then
    echo "❌ Отменено"
    exit 0
fi

echo ""
echo "🔄 Перезапускаю приложение для очистки блокировок в памяти..."
pm2 restart luxon-admin

echo ""
echo "✅ Приложение перезапущено. Все блокировки в памяти очищены."
echo ""
echo "💡 Примечание: Если блокировки хранятся в Redis или БД,"
echo "   их нужно очистить отдельно."
echo ""

