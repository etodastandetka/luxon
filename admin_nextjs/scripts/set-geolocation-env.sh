#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∑–∞–ø–∏—Å–∏ –¥–∞–Ω–Ω—ã—Ö –≥–µ–æ–ª–æ–∫–∞—Ü–∏–∏ –≤ .env —Ñ–∞–π–ª

echo "üó∫Ô∏è  –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –≥–µ–æ–ª–æ–∫–∞—Ü–∏–∏ –≤ .env —Ñ–∞–π–ª–µ"
echo ""

cd /var/www/luxon/admin_nextjs || exit 1

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ .env —Ñ–∞–π–ª–∞
if [ ! -f ".env" ]; then
    echo "‚ö†Ô∏è  –§–∞–π–ª .env –Ω–µ –Ω–∞–π–¥–µ–Ω. –°–æ–∑–¥–∞—é –Ω–æ–≤—ã–π..."
    touch .env
fi

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è/–æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –≤ .env
set_env_var() {
    local key=$1
    local value=$2
    local file=".env"
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —É–∂–µ —ç—Ç–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è
    if grep -q "^${key}=" "$file" 2>/dev/null; then
        # –û–±–Ω–æ–≤–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
        if [[ "$OSTYPE" == "darwin"* ]]; then
            # macOS
            sed -i '' "s|^${key}=.*|${key}=${value}|" "$file"
        else
            # Linux
            sed -i "s|^${key}=.*|${key}=${value}|" "$file"
        fi
        echo "  ‚úÖ –û–±–Ω–æ–≤–ª–µ–Ω–æ: ${key}=${value}"
    else
        # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
        echo "${key}=${value}" >> "$file"
        echo "  ‚úÖ –î–æ–±–∞–≤–ª–µ–Ω–æ: ${key}=${value}"
    fi
}

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
echo "–í–≤–µ–¥–∏—Ç–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —Ä–∞–∑—Ä–µ—à–µ–Ω–Ω–æ–π –∑–æ–Ω—ã:"
echo ""

read -p "–®–∏—Ä–æ—Ç–∞ (latitude) [–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é: 42.84877 –¥–ª—è –ë–∏—à–∫–µ–∫–∞]: " latitude
latitude=${latitude:-42.84877}

read -p "–î–æ–ª–≥–æ—Ç–∞ (longitude) [–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é: 74.57028 –¥–ª—è –ë–∏—à–∫–µ–∫–∞]: " longitude
longitude=${longitude:-74.57028}

read -p "–†–∞–¥–∏—É—Å –≤ –º–µ—Ç—Ä–∞—Ö (radius) [–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é: 10]: " radius
radius=${radius:-10}

# –í–∞–ª–∏–¥–∞—Ü–∏—è
if ! [[ "$latitude" =~ ^-?[0-9]+\.?[0-9]*$ ]]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç —à–∏—Ä–æ—Ç—ã"
    exit 1
fi

if ! [[ "$longitude" =~ ^-?[0-9]+\.?[0-9]*$ ]]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –¥–æ–ª–≥–æ—Ç—ã"
    exit 1
fi

if ! [[ "$radius" =~ ^[0-9]+\.?[0-9]*$ ]] || [ "$radius" -le 0 ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –†–∞–¥–∏—É—Å –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º —á–∏—Å–ª–æ–º"
    exit 1
fi

echo ""
echo "üìù –ó–∞–ø–∏—Å—ã–≤–∞—é –≤ .env —Ñ–∞–π–ª:"
echo ""

# –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
set_env_var "ALLOWED_LATITUDE" "$latitude"
set_env_var "ALLOWED_LONGITUDE" "$longitude"
set_env_var "ALLOWED_RADIUS_METERS" "$radius"

echo ""
echo "‚úÖ –ì–µ–æ–ª–æ–∫–∞—Ü–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∞!"
echo ""
echo "–¢–µ–∫—É—â–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è:"
echo "  ALLOWED_LATITUDE=$latitude"
echo "  ALLOWED_LONGITUDE=$longitude"
echo "  ALLOWED_RADIUS_METERS=$radius"
echo ""
echo "‚ö†Ô∏è  –í–ê–ñ–ù–û: –ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π:"
echo "  pm2 restart luxon-admin"
echo ""

