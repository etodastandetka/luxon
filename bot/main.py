    def _register_middleware(self):
        """–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è middleware"""
        # –í–∫–ª—é—á–∞–µ–º middleware —Å—Ç–∞—Ç—É—Å–∞ –±–æ—Ç–∞ (–ø–∞—É–∑–∞/—Ç–µ—Ö—Ä–∞–±–æ—Ç—ã)
