import { NextRequest, NextResponse } from 'next/server'

const ADMIN_CHAT_ID = process.env.ADMIN_CHAT_ID || '12345' // –ó–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ —Ä–µ–∞–ª—å–Ω—ã–π ID –∞–¥–º–∏–Ω–∞
const BOT_TOKEN = process.env.BOT_TOKEN || 'your_bot_token' // –ó–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ —Ä–µ–∞–ª—å–Ω—ã–π —Ç–æ–∫–µ–Ω –±–æ—Ç–∞

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const { message, user_id, timestamp } = body
    
    if (!message) {
      return NextResponse.json({ error: 'Message is required' }, { status: 400 })
    }
    
    // –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª—è –∞–¥–º–∏–Ω–∞
    const adminMessage = `
üÜò –ù–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –ø–æ–¥–¥–µ—Ä–∂–∫—É

üë§ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: ${user_id}
üìÖ –í—Ä–µ–º—è: ${new Date(timestamp).toLocaleString('ru-RU')}
üí¨ –°–æ–æ–±—â–µ–Ω–∏–µ: ${message}
    `.trim()
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ Telegram –∞–¥–º–∏–Ω—É
    const telegramResponse = await fetch(`https://api.telegram.org/bot${BOT_TOKEN}/sendMessage`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        chat_id: ADMIN_CHAT_ID,
        text: adminMessage,
        parse_mode: 'HTML'
      })
    })
    
    if (!telegramResponse.ok) {
      console.error('Failed to send Telegram message:', await telegramResponse.text())
      return NextResponse.json({ error: 'Failed to send message' }, { status: 500 })
    }
    
    return NextResponse.json({ 
      success: true, 
      message: '–°–æ–æ–±—â–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ –≤ –ø–æ–¥–¥–µ—Ä–∂–∫—É' 
    })
    
  } catch (error) {
    console.error('Support API error:', error)
    return NextResponse.json({ error: 'Internal server error' }, { status: 500 })
  }
}





